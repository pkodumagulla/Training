
public class Mathoper {
	
	static double Addition(double x, double y)
	{
		return (x+y);
	}
	static double Diffrence(double x, double y)
	{
		if(x>y)
			return (x-y);
		else
			return (y-x);
	}
	static double Multiplication (double x, double y)
	{
		return(x*y);
	}
	static double Divsion(double x, double y)
	{
		return(x/y);
	}
}
